class Piece {
    constructor(px, py){
        this.px = px;
        this.py = py;
        this.rotation = 0;
        this.shape = Math.floor(Math.random() * 8);
    }

    rotate(){
        this.rotation = (this.rotation + 1) % 4;
    }

    get(y, x){
        let value = SHAPES[this.shape][this.rotation][y][x];
        return Boolean(value);
    }
}

